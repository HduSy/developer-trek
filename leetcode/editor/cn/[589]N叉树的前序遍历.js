/**
 * // Definition for a Node.
 * function Node(val, children) {
 *    this.val = val;
 *    this.children = children;
 * };
 */

/**
 * @param {Node} root
 * @return {number[]}
 */
var preorder = function (root) {
    if (!root) return []
    let res = []
    function recusion(root) {
        if (!root) return
        res.push(root.val)
        for (let i = 0; i < root.children.length; i++) {
            recusion(root.children[i])
        }
    }
    recusion(root)
    return res
};
